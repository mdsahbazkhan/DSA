function insertionSort(arr){
    let n=arr.length;
    for(let i=0;i<n-1;i++){
        let min=i;
        for(let j=i;j<n;j++){
            if(arr[j]<arr[min]){
                min=j;
            }
        }
        let temp=arr[i];
        arr[i]=arr[min];
        arr[min]=temp
    }
    return arr;
}
console.log(insertionSort([64,25,-12,22,11]));
